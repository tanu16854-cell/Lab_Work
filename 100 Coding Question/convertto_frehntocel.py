# Program to convert Fahrenheit to Celsius

# Taking input from user
fahrenheit = float(input("Enter temperature in Fahrenheit: "))

# Formula: C = (F - 32) Ã— 5/9
celsius = (fahrenheit - 32) * 5/9

# Display result
print("Temperature in Celsius:", celsius)

